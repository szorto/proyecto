    private void jb_guardarPreguntaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_guardarPreguntaMouseClicked
        if (jtf_pregunta.getText().isBlank() == false) {
            if (jrb_verdaderoFalso.isSelected()) {
                if (jrb_verdadero.isSelected()) {
                    //  PtipoVerdaderoFalso pvf = new PtipoVerdaderoFalso(true, jtf_pregunta.getText());
                    exa.getListaPreguntas().add(new PtipoVerdaderoFalso(true, jtf_pregunta.getText(), true));
                } else {
                    PtipoVerdaderoFalso pvf = new PtipoVerdaderoFalso(false, jtf_pregunta.getText(), true);
                    exa.getListaPreguntas().add(pvf);
                }

            } else if (jrb_seleccionMultiple.isSelected()) {
                if (jcb_preg1.isSelected() || jcb_preg2.isSelected() || jcb_preg3.isSelected() || jcb_preg4.isSelected()) {
                    PseleccionMultiple psm = new PseleccionMultiple(jcb_preg1.isSelected(), jcb_preg2.isSelected(), jcb_preg3.isSelected(), jcb_preg4.isSelected(), jtf_preg1.getText(), jtf_preg2.getText(), jtf_preg3.getText(), jtf_preg4.getText(), jtf_pregunta.getText(), true);
                    exa.getListaPreguntas().add(psm);
                }
            } else if (jrb_tipoEnumerada.isSelected()) {
                PtipoEnumerada pte = new PtipoEnumerada(jtf_pregunta.getText(), true);
                String[] aa = jtf_te.getText().split(";");
                for (int i = 0; i < aa.length; i++) {
                    pte.getRespuestas().add(aa[i]);
                }
                exa.getListaPreguntas().add(pte);
            }
        }
        DefaultComboBoxModel m = new DefaultComboBoxModel();
        for (Preguntas t : exa.getListaPreguntas()) {
            m.addElement(t);
        }
        jcb_preguntaExamen.setModel(m);

    }//GEN-LAST:event_jb_guardarPreguntaMouseClicked

